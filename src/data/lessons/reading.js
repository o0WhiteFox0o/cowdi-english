// Bộ sinh "đoạn văn mẫu" cho bài học.
// — Nếu bài học đã có `lesson.reading` (do người biên soạn viết tay) → giữ nguyên.
// — Nếu chưa có → tự ghép từ các `example` trong `vocabulary` thành 1 đoạn ngắn để
//   người học vận dụng từ đã học (đọc hiểu + highlight từ vựng + câu hỏi).
//
// Cấu trúc chuẩn của `lesson.reading`:
// {
//   title: string,          // tiêu đề đoạn văn
//   passage: string,        // đoạn văn tiếng Anh (nhiều câu, cách nhau bằng dấu chấm + space)
//   translation: string,    // dịch nghĩa tiếng Việt (có thể rỗng với đoạn auto-gen)
//   highlights: string[],   // các từ vựng xuất hiện trong đoạn (để highlight)
//   questions: [            // câu hỏi hiểu bài — tuỳ chọn
//     { question, options[4], correct }
//   ],
//   autoGenerated: boolean  // đánh dấu đoạn được tự sinh
// }

/** Escape chuỗi dùng trong RegExp. */
function escapeRegex(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

/** Trả về tập các từ vựng xuất hiện trong `passage` (so sánh không phân biệt hoa/thường). */
function findHighlights(passage, vocabWords) {
  const lower = passage.toLowerCase();
  const seen = [];
  for (const w of vocabWords) {
    if (!w) continue;
    const re = new RegExp(`\\b${escapeRegex(w.toLowerCase())}\\b`);
    if (re.test(lower)) seen.push(w);
  }
  return seen;
}

/** Trộn mảng (không thay đổi mảng gốc). */
function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

/** Sinh 3 câu hỏi trắc nghiệm hiểu bài từ các từ được highlight. */
function buildQuestions(vocabulary, highlights) {
  if (!highlights.length || vocabulary.length < 4) return [];
  const pool = vocabulary.filter((v) => highlights.includes(v.word));
  const picked = shuffle(pool).slice(0, Math.min(3, pool.length));
  const questions = [];
  for (const v of picked) {
    const distractors = shuffle(
      vocabulary.filter((x) => x.word !== v.word && x.meaning)
    ).slice(0, 3);
    if (distractors.length < 3) continue;
    const opts = shuffle([v.meaning, ...distractors.map((d) => d.meaning)]);
    questions.push({
      question: `Trong đoạn văn, từ "${v.word}" có nghĩa là gì?`,
      options: opts,
      correct: opts.indexOf(v.meaning),
    });
  }
  return questions;
}

/**
 * Sinh đoạn văn mẫu từ bài học.
 * Ưu tiên `lesson.reading` đã khai báo sẵn; ngược lại ghép từ `example` của vocabulary.
 */
export function buildPassage(lesson) {
  if (lesson?.reading?.passage) {
    // Đã có sẵn — bổ sung highlights/autoGenerated nếu thiếu
    const r = lesson.reading;
    return {
      title: r.title || `Đoạn văn: ${lesson.title}`,
      passage: r.passage,
      translation: r.translation || '',
      highlights: r.highlights && r.highlights.length
        ? r.highlights
        : findHighlights(r.passage, (lesson.vocabulary || []).map((v) => v.word)),
      questions: r.questions || [],
      autoGenerated: false,
    };
  }

  const vocab = lesson?.vocabulary || [];
  const examples = vocab
    .map((v) => v.example)
    .filter((s) => typeof s === 'string' && s.trim().length > 0);

  if (examples.length < 2) return null;

  // Lấy 5-7 câu ví dụ ngẫu nhiên (ổn định theo id của bài để đoạn văn không đổi mỗi lần render)
  const n = Math.min(examples.length, 6);
  // Seed đơn giản theo id để giữ ổn định
  const seed = (lesson.id || '').split('').reduce((a, c) => a + c.charCodeAt(0), 0);
  const rotated = [...examples.slice(seed % examples.length), ...examples.slice(0, seed % examples.length)];
  const selected = rotated.slice(0, n);

  const passage = selected
    .map((s) => {
      const trimmed = s.trim();
      return /[.!?]$/.test(trimmed) ? trimmed : trimmed + '.';
    })
    .join(' ');

  const highlights = findHighlights(passage, vocab.map((v) => v.word));
  const questions = buildQuestions(vocab, highlights);

  return {
    title: `Đoạn văn luyện tập: ${lesson.title}`,
    passage,
    translation: '', // auto-gen không có dịch — người học click từ để xem nghĩa
    highlights,
    questions,
    autoGenerated: true,
  };
}

/** Gắn `reading` vào mỗi bài học trong mảng. */
export function withReading(lessons) {
  return lessons.map((l) => ({ ...l, reading: buildPassage(l) }));
}
